var char = 'A'
var orangganteng = "Fachri ganteng"
var orangjelek = "Aku"
var p = 1

fun main() {
    println(char)
    while (p<=10) {
        if (p > 5) {
            println(orangganteng)
        } else {
            println(orangjelek)
        }
        p++
    }
    baru()
}

fun baru() {
    var cri = "Dra"
    println(cri)
}
