var umurfachri = 20

fun main() {
    while (umurfachri <=30){
        if (umurfachri <=23){
            println("Saat umur Fachri " + umurfachri + ", Fachri berkeluarga dengan Fatin")
        }else if (umurfachri>23 && umurfachri<=27){
            println("Saat umur Fachri " + umurfachri + ", Fachri berkeluarga dengan Mikaella")
        }else{
            println("Saat umur Fachri " + umurfachri + ", Fachri berkeluarga dengan Nadhirah")
        }
        umurfachri++
    }
}